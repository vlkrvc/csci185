function drawPixelArt(grid, x, y, pixel) {
    let i = 0;
    while (i < grid.length) {
        drawRow(grid[i], x, y, pixel, pallete);
        i++;
        y += pixel;
    }
}
    

function drawRow (row, topX, topY, pixelWidth, 
    pallete=['transparent', 'hotpink', 'black', 'white']) {
    // 0 -> transparent
    // 1 -> pink
    // 2 -> black 
    // 3 -> white

    let i = 0;

    while (i < row.length) {
        
        square(topX, topY, pixelWidth)
        topX += pixelWidth;
        i++;
    }
}
